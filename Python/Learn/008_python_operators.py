# Arithmetic
print(1+2)  # 3
print(2-1)  # 1
print(1*2)  # 2
print(1/2)  # 0.5
print(3%2)  # 1
print(3**2) # 9
print(5//2) # 2

# Assignment
x = 12
print(x)    # 12
x += 2
print(x)    # 14
x -= 2
print(x)    # 12
x *= 2
print(x)    # 24
x /= 2
print(x)    # 12.0
x %= 5
print(x)    # 2.0
x //= 2
print(x)    # 1.0
x**= 2
print(x)    # 1.0

# Comparison
print(1==2)     # False
print(1!= 2)    # True
print(1>2)      # False
print(1<2)      # True
print(1>=2)     # False
print(1<=2)     # True

# Logical
print(1 and 0)  # 0
print(1 or 0)   # 1
print(not 1)    # False

# Identity
x,y = 1,1
print(x is y)     # True
print(x is not y) # False

# Membership
print(1 in [1,2])       # True
print(1 not in [1,2])   # False

# Bitwise
print(1&2)    # 0
print(1|2)    # 3
print(1^2)    # 3
print(~2)     # -3
print(1<<2)   # 4
print(4>>2)   # 1